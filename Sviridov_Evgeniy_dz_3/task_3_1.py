digit_dict = {
    'zero': 'ноль',
    'one': 'один',
    'two': 'два',
    'three': 'три',
    'four': 'четыре',
    'five': 'пять',
    'six': 'шесть',
    'seven': 'семь',
    'eight': 'восемь',
    'nine': 'девять',
    'ten': 'десять'
}


def num_translate(digit):
    return digit_dict[digit] if digit in digit_dict else None


print(num_translate("one"))
print(num_translate("eight"))
print(num_translate(" "))
